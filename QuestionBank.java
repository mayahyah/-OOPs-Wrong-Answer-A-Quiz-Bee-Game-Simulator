    // Question Randomizer
    public static List<Question> generateLevel(List<Question> source, int count) {
        if (source == null) {
            return new ArrayList<>();
        }
        List<Question> copy = new ArrayList<>(source);
        Collections.shuffle(copy);
        if (count >= copy.size()) {
            return copy;
        }
        return new ArrayList<>(copy.subList(0, count));
    }
